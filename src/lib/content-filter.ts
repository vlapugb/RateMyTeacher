export type ContentFilterResult = {
  passed: boolean;
  flags: string[];
};

export function filterDangerousContent(text: string): ContentFilterResult {
  const flags: string[] = [];

  for (const { pattern, label } of OFFENSIVE_PATTERNS) {
    pattern.lastIndex = 0;
    if (pattern.test(text)) {
      flags.push(label);
    }
  }

  for (const pattern of RUSSIAN_MAT_PATTERNS) {
    pattern.lastIndex = 0;
    if (pattern.test(text)) {
      const first = flags[flags.length - 1];
      if (first !== "袧械薪芯褉屑邪褌懈胁薪邪褟 谢械泻褋懈泻邪") {
        flags.push("袧械薪芯褉屑邪褌懈胁薪邪褟 谢械泻褋懈泻邪");
      }
    }
  }

  const uniqueFlags = [...new Set(flags)];

  return {
    passed: uniqueFlags.length === 0,
    flags: uniqueFlags,
  };
}

export function getContentFilterWarning(flags: string[]): string {
  return flags.join("; ");
}
